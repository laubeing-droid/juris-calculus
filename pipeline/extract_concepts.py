# 噪音黑名单
BLACKLIST = {"的","是","在","和","了","等","及","与","或","第","条","款","项","目","编","章","节","原告","被告","第三人","上诉人","被上诉人","法院","法官","审判员","书记员","手机","微信","电话","邮箱","地址","应当","可以","法律","法规","以下","以上","规定","问题","案件","情形","情况","根据","依据","认为",}

def is_valid_concept(c: str) -> bool:
    """过滤噪音：纯数字、百分比、倍数等"""
    if not (3 <= len(c) <= 30):
        return False
    if c in BLACKLIST:
        return False
    # 纯数字/百分比
    if re.fullmatch(r'[\d.%×\s倍成个百分点]+', c):
        return False
    # 纯标点
    if re.fullmatch(r'[\s，,。.]', c):
        return False
    return True

def extract_from_book(fp: Path) -> Counter:
    """按书提取概念"""
    bn = fp.name.replace("_ocr.json", "")
    data = json.loads(fp.read_text(encoding="utf-8"))
    text = "\n".join(p.get("text", "") for p in data.get("pages", []))

    rules = BOOK_RULES.get(bn, [])
    concepts = Counter()
    for pat in rules:
        for m in re.findall(pat, text):
            concept = m if isinstance(m, str) else "".join([x for x in m if isinstance(x, str)][:1])
            concept = concept.strip()
            if is_valid_concept(concept):
                concepts[concept] += 1

    # 去噪（必须出现2次以上）
    return Counter({k: v for k, v in concepts.items() if v >= 2})


def main():
    all_concepts = Counter()
    book_stats = {}

    for fp in sorted(OUT_DIR.glob("*_ocr.json")):
        bn = fp.name.replace("_ocr.json", "")
        concepts = extract_from_book(fp)
        all_concepts.update(concepts)
        book_stats[bn] = len(concepts)
        print(f"  {bn:30s} {len(concepts):>4} 个概念")

    print(f"\n总概念（去重）: {len(all_concepts)}")
    print(f"常用概念 Top 20:")
    for c, n in all_concepts.most_common(20):
        print(f"  {c:30s} ×{n}")

    # 更新 concept_registry
    registry = {k: {"frequency": v, "source": "OCR-per-book"}
                for k, v in all_concepts.most_common(800)}
    (CONFIG_DIR / "concept_registry_ocr.yaml").write_text(
        yaml.dump({"concepts": registry, "_meta": {"total": len(registry), "by_book": book_stats}},
                  allow_unicode=True), encoding="utf-8")

    # 更新 domain_config.yaml
    dc = yaml.safe_load(open(CONFIG_DIR / "domain_config.yaml", encoding="utf-8"))
    current = set(dc.get("concept_registry", []))
    new_concepts = [k for k, _ in all_concepts.most_common(600) if k not in current][:400]
    dc["concept_registry"] = sorted(list(current) + new_concepts)
    (CONFIG_DIR / "domain_config.yaml").write_text(yaml.dump(dc, allow_unicode=True), encoding="utf-8")

    print(f"\ndomain_config.yaml: {len(current)} → {len(dc['concept_registry'])} 个概念")
    print(f"concept_registry_ocr.yaml: {len(registry)} 个概念")

if __name__ == "__main__":
    main()
